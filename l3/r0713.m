% Синтезування смугового фільтру 2-го порядку 
fs = 200 ;
fc = 20;         
r=0.7; % центральна частота
phi = 2*pi*fc/fs;
b = [1 0 -1] ;                    % чисельник CФ 
a =  [1 -2*r*cos(phi) r^2];	% знаменник


%=== Завдання #1.2 ===
% АЧХ та ФЧХ смугового фільтру 2-го порядку
n=512;
figure()
[h,w]=freqz(b,a,n);
mag=abs(h); phase=angle(h)*180/pi;
subplot(211); plot(w/(2*pi)*fs,mag),grid on
subplot(212), plot(w/(2*pi)*fs,unwrap(phase)),grid on
